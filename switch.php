<?php
// Define the variable
$day = "Sunday";

// Switch statement to check the value
switch ($day) {
    case "Sunday":
        echo "Today is Sunday.";
        break;
    case "Monday":
        echo "Today is Monday.";
        break;
    // You can add more days here
    default:
        echo "It is another day of the week.";
        break;
}
?>